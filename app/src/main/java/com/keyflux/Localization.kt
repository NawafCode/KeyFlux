        val lang = Locale.getDefault().language
        val map = translations[lang] ?: translations["en"]!!
        val cleanKey = if (key.startsWith("keyflux_")) key else "keyflux_$key"
        val fallbackKey = if (key.startsWith("keyflux_")) key.removePrefix("keyflux_") else key
        return map[cleanKey] ?: map[fallbackKey] ?: translations["en"]!![cleanKey] ?: translations["en"]!![fallbackKey] ?: key
    }
